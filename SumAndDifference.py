# This program calculates the sum and difference of two numbers entered by the user.
num1 = int(input("enter your first number:"))
num2 = int(input("enter your second number:"))
# The sum is calculated by adding num1 and num2 together.
sum = num1 + num2
# The difference is calculated by subtracting num2 from num1.
difference = num1 - num2
# Finally, the program prints the results to the user.
print("the sum of the two numbers is:", sum)
print("the difference of the two numbers is:", difference)